//! `WakaTime` HTTP client library.
//!
//! This crate provides an async HTTP client for the [`WakaTime` API](https://wakatime.com/developers).
//! It is designed to be usable independently of the `waka` CLI.
//!
//! # Example
//!
//! ```rust,no_run
//! # tokio_test::block_on(async {
//! // Client construction and endpoint calls will be added in task 0.4/0.5.
//! # });
//! ```
